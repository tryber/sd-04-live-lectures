// Como criar uma aplicação React ?

// Vamos usar o pacote create-react-app
// Disponibilizado pelo Facebook (criador do react <3)

//--------------------------------------------------//
// Pra que serve esse pacote ?

// Ele nos dá uma estrutura básica de um projeto React, 
// sem necessidade de configurarmos todo o necessário manualmente.

//--------------------------------------------------//
// Como rodar esse comando create-react-app ?

// npx create-react-app my-app
// npx create-react-app (nomedomeuprojeto)

//--------------------------------------------------//
// O que é esse npx ? -> Package Runner (Executa pacotes)

// npm -> Package Runner e Package Installer

// O npx vem instalado juntamente com o npm 5.2+.
// Ele permite que você execute um create-react-app sem que para 
// isso você tenha que instalar o pacote node globalmente. 
// Isso ajuda porque não te obriga a instalar o pacote que você quer 
// apenas para executá-lo uma vez para criar uma estrutura de um projeto, 
// como nesse caso.


//--------------------------------------------------//
// Estrutura de pastas React que o create-react-app criou

// O mais importante para nós no momento é o diretório src.

// É ali que ficam os arquivos JS que vamos utilizar para programar nosso app.

// É importante também a pasta node_modules que contêm as dependências do seu projeto.


//---------------------------------------------c-----//
// Como vamos rodar essa aplicação?

// Vamos entrar dentro do diretório do App.

// Agora vamos executar npm start.

//--------------------------------------------------//
// Agora vamos ver como empacotar seu App para produção.

// Primeiro vamos executar npm run build.

// Ele gerou uma pasta chamada build que contêm todos os arquivos para o um posterior implantação.

